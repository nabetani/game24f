
import * as G from './game'
import "jest"

([
  [0, 0],
  [1, 1],
  [12, 12],
  [123, 123],
  [1234, 1234],
  [9999, 9999],
  [12344, 12340],
  [12349, 12340],
  [99999, 99990],
  [1234567, 1234000],
  [1000000, 1000000],
] as [number, number][]).forEach(([v, ex]) => {
  test(
    `numText(${v})`, () => expect(G.qdigit(v)).toBe(ex)
  );
});

([
  [0, 0],
  [1, 1],
  [12, 12],
  [123, 120],
  [1234, 1200],
  [9999, 9900],
  [12344, 12000],
  [12349, 12000],
  [99999, 99000],
  [1234567, 1200000],
  [1000000, 1000000],
] as [number, number][]).forEach(([v, ex]) => {
  test(
    `numText(${v})`, () => expect(G.didigit(v)).toBe(ex)
  );
})
